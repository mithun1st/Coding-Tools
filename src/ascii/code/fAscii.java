    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(jTextField1.getText().isEmpty() || jTextField7.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Set The Limit Of Range","Message",JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            int min = Integer.parseInt(jTextField1.getText());
            int max = Integer.parseInt(jTextField7.getText());
            String s = "";
            for (int i = min; i <= max; i++) {
                char c = (char) i;
                s+=i+""+" = "+String.valueOf(c)+"\n";
            }
            jTextPane1.setText(s);
            jTextPane1.enable();
        }
    }//GEN-LAST:event_jButton3ActionPerformed


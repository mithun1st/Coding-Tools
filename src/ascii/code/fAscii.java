    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            if(jTextField4.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Ascii Field is Empty","Message",JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                String s=jTextField4.getText();
                char c = (char) Integer.parseInt(s);
                jTextField5.setText(String.valueOf(c));
                jTextField5.enable();
            }
        } 
        catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Input Error !","Warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

